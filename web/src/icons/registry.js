export function resolveIconName(iconName) {
  const value = String(iconName || '').trim()
  if (!value) return DEFAULT_CATEGORY_ICON
  if (ICON_URLS[value]) return value

  const appMapped = APP_LEGACY_ICON_MAP[value]
  if (appMapped && ICON_URLS[appMapped]) {
    return appMapped
  }

  const categoryMapped = normalizeCategoryIconName(value)
  if (ICON_URLS[categoryMapped]) {
    return categoryMapped
  }

  return DEFAULT_CATEGORY_ICON
}

export function resolveIconUrl(iconName) {
  return (
    ICON_URLS[resolveIconName(iconName)] || ICON_URLS[DEFAULT_CATEGORY_ICON]
  )
}

export function resolveIconMarkup(iconName) {
  return resolveIconUrl(iconName)
}
